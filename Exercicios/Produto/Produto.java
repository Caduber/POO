public class Produto{

    int id;
    String nome;
    float preco;
    

    //GETTERS

    public int getId(){
        return id;
    }

    public String getNome(){
        return nome;
    }

    public float getPreco(){
        return preco;
    }

    //SETTERS

    public void setId(int id){
        this.id = id;
    }

    public void setNome(String nome){
        this.nome = nome;
    }

    public void setPreco(float preco){
        this.preco = preco;
    }

}